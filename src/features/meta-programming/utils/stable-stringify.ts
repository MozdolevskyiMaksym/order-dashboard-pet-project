// Це покращена версія Json.stringify, яка сортує ключі об’єктів і обробляє циклічні посилання,
// а також різні типи даних, щоб створити однакове стабільне рядкове представлення будь-якого значення.
// Це корисно для генерації ключів кешу або для логування, коли потрібно отримати консистентне представлення складних структур даних.

// Функція stableStringify приймає будь-яке значення і повертає його стабільне рядкове представлення,
// яке можна використовувати для генерації ключів кешу або для логування.
// Вона обробляє різні типи даних, включаючи циклічні посилання,
// і забезпечує консистентний порядок властивостей об’єктів для однакових структур даних.
export default function stableStringify(value: unknown): string {
  const seen = new WeakMap<object, number>(); // Використовуємо WeakMap для відстеження об’єктів, які вже були серіалізовані, щоб уникнути циклічних посилань
  let seenId = 0; // Лічильник для присвоєння унікальних ідентифікаторів об’єктам, які були серіалізовані

  // Внутрішня рекурсивна функція serialize, яка виконує фактичну серіалізацію значення,
  // обробляючи різні типи даних та структури
  function serialize(value: unknown): unknown {
    // Обробка примітивних типів даних (рядки, числа, булеві значення) та null
    if (value === null) {
      return null;
    }

    const t = typeof value; // Отримуємо тип значення для визначення, як його серіалізувати

    // Для примітивних типів даних (рядки, числа, булеві значення) повертаємо їх без змін, оскільки вони вже є серіалізованими унікальними представленнями
    if (t === "string" || t === "number" || t === "boolean") {
      return value;
    }

    // Обробка інших типів даних, таких як undefined, bigint, функції, дати, масиви та об’єкти
    if (t === "undefined") {
      return "__undefined__";
    }

    // Для типу bigint повертаємо рядкове представлення з префіксом, щоб відрізнити його від звичайних рядків
    if (t === "bigint") {
      return `__bigint__:${String(value)}`;
    }

    // Для типу symbol повертаємо рядкове представлення з префіксом, щоб відрізнити його від звичайних рядків
    if (t === "function") {
      return `__function__:${(value as Function).name || "anonymous"}`;
    }

    // Для типу symbol повертаємо рядкове представлення з префіксом, щоб відрізнити його від звичайних рядків
    if (value instanceof Date) {
      return `__date__:${value.toISOString()}`;
    }

    // Для масивів рекурсивно серіалізуємо кожен елемент масиву, зберігаючи порядок елементів
    if (Array.isArray(value)) {
      return value.map((x) => serialize(x));
    }

    // Для об’єктів перевіряємо, чи ми вже бачили цей об’єкт (для обробки циклічних посилань)
    if (t === "object") {
      const obj = value as Record<string, unknown>;

      // Якщо об’єкт вже був серіалізований, повертаємо спеціальне рядкове представлення з унікальним ідентифікатором, щоб позначити циклічне посилання
      if (seen.has(obj)) {
        return `__cycle__:${seen.get(obj)}`;
      }

      seenId += 1; // Присвоюємо новий унікальний ідентифікатор для цього об’єкта
      seen.set(obj, seenId); // Додаємо об’єкт до WeakMap, щоб відстежувати його серіалізацію

      // Сортуємо ключі об’єкта в алфавітному порядку для забезпечення стабільного порядку властивостей у серіалізованому представленні
      const keys = Object.keys(obj).sort((a, b) => a.localeCompare(b));
      const out: Record<string, unknown> = {}; // Створюємо новий об’єкт для зберігання серіалізованих властивостей

      // Рекурсивно серіалізуємо кожну властивість об’єкта, зберігаючи стабільний порядок ключів
      for (const key of keys) {
        out[key] = serialize(obj[key]); // Серіалізуємо значення властивості та зберігаємо його в новому об’єкті out
      }

      return out; // Повертаємо серіалізований об’єкт з відсортованими ключами та серіалізованими значеннями
    }

    return `__unknown__:${String(value)}`; // Для будь-яких інших типів даних, які не були оброблені вище, повертаємо рядкове представлення з префіксом, щоб позначити їх як невідомі типи
  }

  // Виконуємо серіалізацію значення за допомогою внутрішньої функції serialize і перетворюємо результат у рядок JSON для отримання стабільного рядкового представлення
  return JSON.stringify(serialize(value));
}

// Наприклад

// JSON.stringify:
// JSON.stringify({b:2, a:1}) - {"b":2,"a":1}
// JSON.stringify({a:1, b:2}) - {"a":1,"b":2}

// хоча обидва об’єкти мають однакові властивості, але в різному порядку,
// JSON.stringify повертає різні рядки, що може бути проблемою для генерації ключів кешу або для логування.

// stableStringify:
// stableStringify({b:2, a:1}) - {"a":1,"b":2}
// stableStringify({a:1, b:2}) - {"a":1,"b":2}

// обидва об’єкти мають однакові властивості, і stableStringify повертає однаковий рядок для обох,
// що робить його більш надійним для генерації ключів кешу або для логування.
