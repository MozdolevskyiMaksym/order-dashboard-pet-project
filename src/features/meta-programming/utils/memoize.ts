import { MemoizedFn, MemoizeOptions } from "../types";
import stableStringify from "./stable-stringify";

// Функція memoize приймає функцію fn та опції для налаштування мемоізації,
// і повертає обгорнуту версію цієї функції з кешуванням результатів
export default function memoize<TArgs extends ReadonlyArray<unknown>, TResult>(
  fn: (...args: TArgs) => TResult,
  options: MemoizeOptions<TArgs> = {},
): MemoizedFn<TArgs, TResult> {
  // Функція для генерації ключа кешу на основі аргументів функції.
  // За замовчуванням використовує stableStringify для створення унікального рядка з аргументів.
  const keyFn = options.keyFn ?? ((args) => stableStringify(args));

  const maxEntries = options.maxEntries ?? 200; // Максимальна кількість записів у кеші, після досягнення якої найстаріші записи будуть видалені

  // Використовуємо Map для зберігання кешу, оскільки він зберігає порядок вставки,
  // що дозволяє реалізувати LRU (Least Recently Used) механізм видалення старих записів
  const cache = new Map<string, TResult>(); 

  let hits = 0; // Лічильник попадань у кеш (коли результат для даних аргументів вже є в кеші)
  let misses = 0; // Лічильник промахів кеша (коли результат для даних аргументів відсутній в кеші і функція fn повинна бути викликана для обчислення результату)

  // Обгорнута версія функції fn, яка реалізує мемоізацію. Вона приймає ті ж аргументи, що і оригінальна функція, і повертає результат з кешу або обчислює його, якщо його немає в кеші.
  const wrapped = ((...args: TArgs) => {
    const key = keyFn(args); // Генеруємо ключ для кешу на основі аргументів

    // Перевіряємо, чи є результат для цього ключа в кеші
    if (cache.has(key)) {
      hits += 1; // Якщо результат є в кеші, збільшуємо лічильник попадань
      const value = cache.get(key) as TResult; // Отримуємо значення з кешу

      // Оновлюємо порядок записів у кеші, видаляючи і знову додаючи ключ, щоб він став найновішим (для реалізації LRU механізму)
      // LRU = Least Recently Used (“найменш нещодавно використаний”).
      // Це стратегія кеша: коли кеш переповнюється, ми викидаємо той запис, яким найдовше не користувались.
      cache.delete(key);
      cache.set(key, value);

      return value;
    }

    misses += 1; // Якщо результату немає в кеші, збільшуємо лічильник промахів
    const result = fn(...args); // Викликаємо оригінальну функцію для обчислення результату

    cache.set(key, result); // Зберігаємо результат у кеші з відповідним ключем

    // Якщо кількість записів у кеші перевищує максимальну кількість, видаляємо найстаріший запис (перший ключ у Map)
    if (cache.size > maxEntries) {
      const firstKey = cache.keys().next().value; // Отримуємо перший ключ з кешу, який є найстарішим записом
      // .next() - повертає об’єкт з двома властивостями: value (значення поточного ключа) та done (булеве значення, яке вказує, чи досягнуто кінця ітерації). Ми використовуємо .value, щоб отримати сам ключ.

      // Видаляємо найстаріший запис з кешу, щоб звільнити місце для нових записів
      if (firstKey) {
        cache.delete(firstKey); // Видаляємо найстаріший запис з кешу
      }
    }

    return result; // Повертаємо обчислений результат
  }) as MemoizedFn<TArgs, TResult>;

  wrapped.cacheSize = () => cache.size; // Метод для отримання поточного розміру кешу (кількості записів у кеші)

  // Метод для очищення кешу, який видаляє всі записи з кешу та скидає лічильники попадань і промахів
  wrapped.cacheClear = () => {
    cache.clear();
    hits = 0;
    misses = 0;
  };
  wrapped.cacheHits = () => hits; // Метод для отримання кількості попадань у кеш (скільки разів результат був знайдений у кеші)
  wrapped.cacheMisses = () => misses; // Метод для отримання кількості промахів кеша (скільки разів результат не був знайдений у кеші і функція fn була викликана для обчислення результату)

  return wrapped; // Повертаємо обгорнуту версію функції з мемоізацією та додатковими методами для роботи з кешем
}

// Функція memoize приймає будь-яку функцію fn і повертає її обгорнуту версію з кешуванням результатів.
// Коли обгорнута функція викликається, вона спочатку створює ключ кешу на основі аргументів (keyFn, за замовчуванням через stableStringify).
// Далі вона перевіряє, чи є вже результат для такого ключа в Map.

// Якщо результат є — це cache hit: функція не викликає fn повторно, а одразу повертає значення з кешу.
// Якщо результату немає — це cache miss: викликається оригінальна функція fn, результат зберігається в кеш і повертається.

